def build_all_attributes() -> list[dict]:
    builders: list[Callable[[], list[dict]]] = [
        build_global_attributes,
        _attrs_mattresses,
        _attrs_beds,
        _attrs_bed_bases,
    ]
    builders.extend(category_attribute_builders)

    rows: list[dict] = []
    for fn in builders:
        rows.extend(fn())

    for i, row in enumerate(rows, start=1):
        row["id"] = f"attr_{i:03d}"

    return rows
